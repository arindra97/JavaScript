// Variable
/*
    Since EcmaScript6 variable at JS 
    declare with let or const and not using
    var anymore, because var can cause bug code
*/ 
let firstName;
firstName = "Muhammad Arindra";

console.log(firstName)

// variable let can be declare more than one declaration
firstName = "Arindra";
console.log(firstName)

// variable const can be declare only one 
const lastName = "Khoiru Syawal";

// if we declare const variable more than one it can be error
lastName = "Syawal";
console.log(lastName)


